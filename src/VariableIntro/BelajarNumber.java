package VariableIntro;

public class BelajarNumber {
    public static void main(String[] args){

        // Belajar Short - 32768 to 32767
        short umur = 100;

        // Belajar Int - 2147483648 to 2147483647
        int hargaBarang = 100_000_000;

        // Belajar long -9,223,372,036,854,775,806 to 9,223,372,036,854,775,807
        long hargaPulau = 2_000_000_000_000L;

        // Belajar Floot , 7 decimal digit
        float nilaiDecimal1 = 3.14122345678f;

        // Belajar double, 16 decimal digit
        double nilaiDecimal2 = 3.1234567890987654;
    }
}
